<template>
    <div class="login-wrap">
        <div class="ms-login">
            <div class="ms-title">后台管理系统</div>
            <el-form :model="param" :rules="rules" ref="login" label-width="0px" class="ms-content">
                <el-form-item prop="username">
                    <el-input v-model.trim="param.username" placeholder="username">
                        <el-button slot="prepend" icon="el-icon-lx-people"></el-button>
                    </el-input>
                </el-form-item>
                <el-form-item prop="password">
                    <el-input
                        type="password"
                        placeholder="password"
                        v-model.trim="param.password"
                        @keyup.enter.native="submitForm()"
                    >
                        <el-button slot="prepend" icon="el-icon-lx-lock"></el-button>
                    </el-input>
                </el-form-item>
                <div class="login-btn">
                    <el-button type="primary" @click="submitForm()">{{content}}</el-button>
                </div>
                <div class="login-tips" @click="regist()">
                    没有账号?点击这里注册
                </div>
            </el-form>
        </div>
    </div>
</template>

<script>
import {login} from '../../api/login';
import Cookie from 'js-cookie';
export default {
    data: function() {
        return {
            content: "登录",
            param: {
                username: '',
                password: ''
            },
            rules: {
                username: [{ required: true, message: '请输入用户名', trigger: 'blur' }],
                password: [{ required: true, message: '请输入密码', trigger: 'blur' }]
            }
        };
    },
    methods: {
        regist() {
            this.content = "注册"
        },
        submitForm() {
            if (this.param.username == '') {
                this.$message("please input username");
                return false;
            }
            if (this.param.password == '') {
                this.$message("please input password");
                return false;
            }
            login(this.param).then(res => {
                if (res.status == 200) {
                    Cookie.set("access-token", res.data);
                    this.$router.push('/');
                }
            }, err => {
            })
        }
    }
};
</script>

